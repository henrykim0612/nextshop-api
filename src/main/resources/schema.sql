    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT ck_gender CHECK (gender IN ('M', 'F')),
    CONSTRAINT uq_user_email UNIQUE (email)
);

