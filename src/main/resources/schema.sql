    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_name VARCHAR(20) NOT NULL,
    CONSTRAINT uq_category_name UNIQUE (category_name),
    CONSTRAINT ck_category_name CHECK (category_name IN ('WOMEN', 'MEN'))
);

